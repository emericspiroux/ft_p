#include "ft_p.h"

int				wait_response(int sock)
{
	char		buf[1024];
	int			r;

	ft_bzero(buf, 1023);
	if ((r = read(sock, buf, 1023)) > 0)
	{
		buf[r] = '\0';
		ft_bzero(buf, 1023);
	}
	return (1);
}
