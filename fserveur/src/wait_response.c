#include "ft_p.h"

void			wait_response(int cs)
{
	char		buf[1024];
	int			r;

	if ((r = read(cs, buf, 1023)) > 0)
	{
		buf[r] = '\0';
		ft_bzero(buf, 1023);
	}
	ft_bzero(buf, 1023);
}
